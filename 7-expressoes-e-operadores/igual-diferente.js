/*
    Operadores de comparação
        * irá comparar valores e retornar um boolean
        como resposta à comparação

    == igual a
    != diferente de
    === estritamente igual a
    !== estritamente diferente de
*/

let one = 1
let two = 2

console.log(two == 1)
console.log(one == '1') 

console.log(one != two) 
console.log(one != 1) 
console.log(one != '1')

console.log(one === '1')
console.log(one === 1)   
console.log(two !== '2')
console.log(two !== 2) 
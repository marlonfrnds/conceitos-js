/*
    Operadores de comparação manior e menor (igual)

    > maior que
    < menor que

    >= maior igual a
    <= menor igual a
*/

let one = 1
let two = 2

console.log(one > two);
console.log(one < two);

console.log(one >= 1);
console.log(two >= 1);

console.log(one <= two);
console.log(one <= 1);
console.log(one <= 0);